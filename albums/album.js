// Onclick displays the Nas details and hides others //
function myAlbum() {
    var a = document.getElementById("artist-details");
    var b = document.getElementById("artist1");
    var c = document.getElementById("artist2");
    var d = document.getElementById("artist3");
    var e = document.getElementById("artist4");
    var f = document.getElementById("artist5");
    var g = document.getElementById("artist6");
    if (a.style.display == "none") {
        a.style.display = "block";
        b.style.display = "none";
        c.style.display = "none";
        d.style.display = "none";
        e.style.display = "none";
        f.style.display = "none";
        g.style.display = "none";
    }
    else {
        a.style.display = "none";
        b.style.display = "block";
        c.style.display = "block";
        d.style.display = "block";
        e.style.display = "block";
        f.style.display = "block";
        g.style.display = "block";
    }
}
